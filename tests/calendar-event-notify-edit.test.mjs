import assert from 'node:assert/strict'
import { readFile } from 'node:fs/promises'
import test from 'node:test'

const migrationUrl = new URL('../supabase/migrations/20260714135523_calendar_event_notify_edit_repair.sql', import.meta.url)
const calendarDomainUrl = new URL('../src/lib/domain/calendar-events.js', import.meta.url)
const sessionsPageUrl = new URL('../src/pages/SessionsPage.jsx', import.meta.url)
const parentPortalPageUrl = new URL('../src/pages/ParentPortalPage.jsx', import.meta.url)
const calendarInvitesDomainUrl = new URL('../src/lib/domain/calendar-event-invites.js', import.meta.url)
const notificationStatusUrl = new URL('../src/lib/domain/calendar-notification-status.js', import.meta.url)

const [migration, calendarDomain, sessionsPage, parentPortalPage, calendarInvitesDomain, notificationStatus] = await Promise.all([
  readFile(migrationUrl, 'utf8'),
  readFile(calendarDomainUrl, 'utf8'),
  readFile(sessionsPageUrl, 'utf8'),
  readFile(parentPortalPageUrl, 'utf8'),
  readFile(calendarInvitesDomainUrl, 'utf8'),
  readFile(notificationStatusUrl, 'utf8'),
])

test('Calendar revisions are generated by the database and ignore no-op double saves', () => {
  assert.match(migration, /add column if not exists notification_revision bigint not null default 1/i)
  assert.match(migration, /row\([\s\S]*new\.title[\s\S]*new\.starts_at[\s\S]*new\.parent_audience[\s\S]*\) is distinct from row\(/i)
  assert.match(migration, /new\.notification_revision = old\.notification_revision \+ 1/i)
  assert.match(migration, /else[\s\S]*new\.notification_revision = old\.notification_revision/i)
  assert.match(migration, /before update on public\.calendar_events/i)
})

test('notification identity allows later revisions while deduplicating retries per recipient', () => {
  assert.match(migration, /calendar_event_id,[\s\S]*event_revision,[\s\S]*notification_type,[\s\S]*lower\(recipient_email\)/i)
  assert.match(migration, /calendar-event:', event_record\.id[\s\S]*':revision:', event_record\.notification_revision/i)
  assert.match(migration, /if notification_record\.status = 'queued'[\s\S]*duplicate_count := duplicate_count \+ 1/i)
  assert.match(migration, /status = 'failed'[\s\S]*last_error = left\(sqlerrm, 1000\)/i)
  assert.match(migration, /notification_record\.status = 'queued'[\s\S]*continue/i)
})

