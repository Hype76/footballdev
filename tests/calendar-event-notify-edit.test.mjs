  assert.match(migration, /from public\.team_staff staff[\s\S]*staff\.team_id = event_record\.team_id[\s\S]*staff\.user_id = actor\.id/i)
  assert.match(migration, /event_record\.cancelled_at is not null/i)
  assert.match(migration, /event_record\.parent_audience not in \('involved_players', 'all_team_parents'\)/i)
